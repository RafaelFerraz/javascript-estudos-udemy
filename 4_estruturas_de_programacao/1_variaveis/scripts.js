//Definição de uma variável
let nome = "Rafael";
console.log(nome);

//Mesclar string com variáveis usando template literals
console.log(`O meu nome é ${nome}!`);

//Operações com variáveis
let laranjas = 5;
console.log(laranjas * laranjas);

//Reatribuição de valores em variáveis já declaradas
nome = "João";
console.log(nome);
laranjas = 854123;
console.log(laranjas);

//Alteração de tipo de dados em variáveis já declaradas
laranjas = "Laranja";
console.log(laranjas);

//Declaração de múltiplas variáveis
let um = 1, dois = 2, tres = 3;
console.log(um + dois + tres);